'use strict';

/**
 * ide-integrator.js — Place H.K-UP config files for each selected IDE
 *
 * Claude Code receives a proper CLAUDE.md section.
 * All other IDEs receive a generic reference file.
 *
 * Export: async function integrate(selectedIDEs, targetDir, hkupFolder)
 */

const fs   = require('node:fs');
const path = require('node:path');
const yaml = require('js-yaml');

const PLATFORMS_FILE = path.join(__dirname, '../data/ide-platforms.yaml');

// ─── YAML loader ─────────────────────────────────────────────────────────────

/**
 * Load IDE definitions from ide-platforms.yaml.
 * @returns {{ [id: string]: IDEPlatform }}
 */
function loadPlatforms() {
  const raw = fs.readFileSync(PLATFORMS_FILE, 'utf8');
  const parsed = yaml.load(raw);
  return parsed.platforms;
}

// ─── Content generators ───────────────────────────────────────────────────────

/**
 * Generate the CLAUDE.md snippet that loads the H.K-UP skill for Claude Code.
 *
 * @param {string} hkupFolder - Name of the hkup folder (e.g. "_hkup")
 * @returns {string}
 */
function generateClaudeCodeContent(hkupFolder) {
  return [
    '# H.K-UP — Claude Code Integration',
    '',
    '## H.K-UP Workflow',
    '',
    'This project uses the H.K-UP workflow system.',
    '',
    `Workflow files are located in: ${hkupFolder}/`,
    '',
    '### Quick start',
    '',
    'Type `/hkup-start` to begin. The Eclaireur agent will scan your project',
    'and guide you through the available workflows.',
    '',
    '### Available workflows',
    '',
    '- `/hkup-start`         — Launch the Eclaireur (project scan + onboarding)',
    '- `/hkup-brainstorm`    — Brainstorming session (Stratege)',
    '- `/hkup-prd`           — Write a Product Requirements Document (Stratege)',
    '- `/hkup-architecture`  — Design system architecture (Architecte)',
    '- `/hkup-design`        — UI/UX design workflow (Designer)',
    '- `/hkup-dev`           — Implementation session (Chirurgien)',
    '- `/hkup-review`        — Code review and quality gate (Gardien)',
    '- `/hkup-security`      — Security audit (Nyx)',
    '',
    '### Documentation',
    '',
    `Global rules     : ${hkupFolder}/data/global-rules.md`,
    `Agent definitions: ${hkupFolder}/agents/`,
    `Workflow steps   : ${hkupFolder}/workflows/`,
    '',
    '<!-- Generated by H.K-UP installer — do not delete this section -->',
  ].join('\n');
}

/**
 * Generate a generic reference file for non-Claude-Code IDEs.
 *
 * @param {string} ideName    - Display name of the IDE
 * @param {string} hkupFolder - Name of the hkup folder (e.g. "_hkup")
 * @returns {string}
 */
function generateGenericContent(ideName, hkupFolder) {
  return [
    `# H.K-UP Configuration for ${ideName}`,
    '',
    '# This project uses the H.K-UP workflow system.',
    `# Workflow files  : ${hkupFolder}/`,
    '# Quick start     : /hkup-start',
    `# Documentation   : ${hkupFolder}/data/global-rules.md`,
    `# Agents          : ${hkupFolder}/agents/`,
    `# Workflows       : ${hkupFolder}/workflows/`,
    '',
    '# Generated by H.K-UP installer.',
  ].join('\n');
}

/**
 * Generate a JSON reference file for IDEs that expect JSON config (e.g. Crush).
 *
 * @param {string} ideName
 * @param {string} hkupFolder
 * @returns {string}
 */
function generateJsonContent(ideName, hkupFolder) {
  const obj = {
    'hkup': {
      'name': ideName,
      'workflow_dir': hkupFolder,
      'quickstart': '/hkup-start',
      'documentation': `${hkupFolder}/data/global-rules.md`,
    },
  };
  return JSON.stringify(obj, null, 2);
}

/**
 * Generate a TOML reference file for IDEs that expect TOML config (e.g. Mistral Vibe).
 *
 * @param {string} ideName
 * @param {string} hkupFolder
 * @returns {string}
 */
function generateTomlContent(ideName, hkupFolder) {
  return [
    `# H.K-UP Configuration for ${ideName}`,
    '',
    '[hkup]',
    `name          = "${ideName}"`,
    `workflow_dir  = "${hkupFolder}"`,
    `quickstart    = "/hkup-start"`,
    `documentation = "${hkupFolder}/data/global-rules.md"`,
    '',
    '# Generated by H.K-UP installer.',
  ].join('\n');
}

// ─── Content dispatcher ───────────────────────────────────────────────────────

/**
 * Select and generate the appropriate content for a given source key.
 *
 * @param {string} sourceKey  - The `source` field from ide-platforms.yaml
 * @param {string} ideName    - Display name of the IDE
 * @param {string} ideId      - Platform ID (e.g. "claude-code")
 * @param {string} hkupFolder - Hkup folder name
 * @returns {string|null} File content, or null if the target is a directory (skip write)
 */
function buildContent(sourceKey, ideName, ideId, hkupFolder) {
  if (sourceKey.endsWith('/')) {
    // Directory copy — nothing to write as a single file
    return null;
  }

  if (ideId === 'claude-code') {
    return generateClaudeCodeContent(hkupFolder);
  }

  if (sourceKey.endsWith('.json')) {
    return generateJsonContent(ideName, hkupFolder);
  }

  if (sourceKey.endsWith('.toml')) {
    return generateTomlContent(ideName, hkupFolder);
  }

  return generateGenericContent(ideName, hkupFolder);
}

// ─── File writer ──────────────────────────────────────────────────────────────

/**
 * Ensure a directory exists, creating it recursively if needed.
 * @param {string} dirPath
 */
function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

/**
 * Write content to a file, creating parent directories as needed.
 * Does not overwrite if the file already exists (appends a H.K-UP section for CLAUDE.md).
 *
 * @param {string} filePath  - Absolute path to the target file
 * @param {string} content   - Content to write
 * @param {string} ideId     - Platform ID (used to decide merge vs overwrite)
 */
function writeConfigFile(filePath, content, ideId) {
  ensureDir(path.dirname(filePath));

  if (ideId === 'claude-code' && fs.existsSync(filePath)) {
    // Append the H.K-UP section to an existing CLAUDE.md rather than overwriting it
    const existing = fs.readFileSync(filePath, 'utf8');
    if (existing.includes('H.K-UP')) {
      // Section already present — skip to avoid duplicates
      return;
    }
    fs.appendFileSync(filePath, '\n\n' + content + '\n', 'utf8');
    return;
  }

  fs.writeFileSync(filePath, content + '\n', 'utf8');
}

// ─── Public API ───────────────────────────────────────────────────────────────

/**
 * Place IDE config files for every selected IDE.
 *
 * @param {string[]} selectedIDEs - Array of platform IDs (e.g. ["claude-code", "cursor"])
 * @param {string}   targetDir    - Absolute path to the project root
 * @param {string}   hkupFolder   - Name of the hkup folder (e.g. "_hkup")
 * @returns {Promise<{ ide: string, files: string[], skipped: string[] }[]>}
 */
async function integrate(selectedIDEs, targetDir, hkupFolder) {
  const platforms = loadPlatforms();
  const results = [];

  for (const ideId of selectedIDEs) {
    const platform = platforms[ideId];
    if (!platform) {
      results.push({ ide: ideId, files: [], skipped: [`Unknown platform: ${ideId}`] });
      continue;
    }

    const written  = [];
    const skipped  = [];

    for (const fileEntry of platform.files) {
      const content = buildContent(fileEntry.source, platform.name, ideId, hkupFolder);

      if (content === null) {
        // Directory entry — nothing to write here (handled by the file copier in main installer)
        skipped.push(fileEntry.target);
        continue;
      }

      const absTarget = path.join(targetDir, fileEntry.target);
      writeConfigFile(absTarget, content, ideId);
      written.push(fileEntry.target);
    }

    results.push({ ide: ideId, name: platform.name, files: written, skipped });
  }

  return results;
}

module.exports = { integrate };
